package day02variables;

public class Variabletekrar {
    public static void main(String[] args) {
        // örnek Char data type inda bir ismin ilk harfi olarak bir variable olustururnuz ve bir deyer atayiniz

        char isminIlkHarfi= 'A';
        System.out.println(isminIlkHarfi);

        //Örnek: boolean data type inde emeklimisin sorusu icin bir variable olusturun ve false deyerini atayiniz
        boolean emeklimisin = false;
        System.out.println(emeklimisin);

        //byte data typnde ogrenci yasi icin bir variable olusturunuz ve deyer atayiniz
        byte ögrenciYasi= 13;
        System.out.println(ögrenciYasi);

        //Ornek 4: Site nufusu icin bir variable olusturup deger atayiniz.
        short siteNufusu= 2500;
        System.out.println(siteNufusu);

        //Ornek 5: Ulke nufusu icin bir variable olusturup deger atamasi yapiniz
        int ulkeNufusu= 80000;
        System.out.println(ulkeNufusu);

        //Ornek 6: iNsan vucudundaki hucre sayisi icin variable olusturup deger atamasi yapiniz
        long hucreSayiyi= 24556778893939L;
        System.out.println(hucreSayiyi);

        //Ornek 7: Gomlek fiyati icin bir tane variable olusturun
        float gömlekFiyati= 23.99F;
        System.out.println(gömlekFiyati);

        //örnek: Hucre agirligi icin bir tane variable olustrun.
        double hucreAgirligi= 0.1234566777788;
        System.out.println(hucreAgirligi);

        // ornek9: ogrenci ismi icin variable olusturup deyer olarrak Ali Can atayiniz
        //
         String ögrenciAdi= "Ali Can";
        System.out.println(ögrenciAdi);
    }


}
